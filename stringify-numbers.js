function stringifyNumbers(obj) {
  for (let item of Object.entries(obj)) {
    if (typeof item[1] === "number") {
      obj[item[0]] = item[1].toString();
    } else {
      stringifyNumbers(item[1]);
    }
  }
  return obj;
}

